#Asignamos a las variables a y b los valores ingresados por el usuario, 
# convirtiendolos a tipo float para permitir decimales.
a = float(input("Ingrese el primer numero: "))
b = float(input("Ingrese el segundo numero: "))

#Falta hacer la resta de a y b, y almacenar el resultado en la variable resultado.
resultado = a - b
#Imprimimos el resultado de la resta.
print("El resultado de la resta es: ", resultado)
